package MyBean;

public class JavaBeanScore {
	private String name;
	private int id;
	private String sex;
	private String department;
	private String major;
	private int math_score;
	private int english_score;
	private int java_score;
	private int c_score;
	private int operating_score;
	private int javaee_score;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getDepartment() {
		return department;
	}

	public void setDepartment(String department) {
		this.department = department;
	}

	public String getMajor() {
		return major;
	}

	public void setMajor(String major) {
		this.major = major;
	}

	public int getMath_score() {
		return math_score;
	}

	public void setMath_score(int math_score) {
		this.math_score = math_score;
	}

	public int getEnglish_score() {
		return english_score;
	}

	public void setEnglish_score(int english_score) {
		this.english_score = english_score;
	}

	public int getJava_score() {
		return java_score;
	}

	public void setJava_score(int java_score) {
		this.java_score = java_score;
	}

	public int getC_score() {
		return c_score;
	}

	public void setC_score(int c_score) {
		this.c_score = c_score;
	}

	public int getOperating_score() {
		return operating_score;
	}

	public void setOperating_score(int operating_score) {
		this.operating_score = operating_score;
	}

	public int getJavaee_score() {
		return javaee_score;
	}

	public void setJavaee_score(int javaee_score) {
		this.javaee_score = javaee_score;
	}
}

